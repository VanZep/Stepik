"""
На вход программе подаётся последовательность целых чисел от 1 до 5,
характеризующее оценку ученика, каждое число на отдельной строке.
Концом последовательности является любое неположительное число либо число,
большее 5. Напишите программу, которая выводит количество пятерок.

Формат входных данных
На вход программе подаётся последовательность чисел, каждое число на отдельной
строке.

Формат выходных данных
Программа должна вывести количество пятерок.

Примечание. Не забывайте, что неположительными числами являются все
отрицательные числа и число 0.

Тестовые данные

Sample Input 1:
1
1
2
2
3
4
4
5
5
5
5
-17
1
2
5
4

Sample Output 1:
4

Sample Input 2:
5
5
-9
5
5

Sample Output 2:
2

Sample Input 3:
5
1
0
5
5
3
-2

Sample Output 3:
1
"""

num = int(input())
count = 0

while 0 < num < 6:
    if num == 5:
        count += 1

    num = int(input())

print(count)
